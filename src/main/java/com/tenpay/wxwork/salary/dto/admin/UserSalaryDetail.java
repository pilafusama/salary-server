package com.tenpay.wxwork.salary.dto.admin;


public class UserSalaryDetail {
    private String salary_overview_id;
    private String salary_detail_id;
    private String category;
    private String detail_name;
    private String detail_amount;
    private String is_read;

    public String getSalary_overview_id() {
        return salary_overview_id;
    }

    public void setSalary_overview_id(String salary_overview_id) {
        this.salary_overview_id = salary_overview_id;
    }

    public String getSalary_detail_id() {
        return salary_detail_id;
    }

    public void setSalary_detail_id(String salary_detail_id) {
        this.salary_detail_id = salary_detail_id;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDetail_name() {
        return detail_name;
    }

    public void setDetail_name(String detail_name) {
        this.detail_name = detail_name;
    }

    public String getDetail_amount() {
        return detail_amount;
    }

    public void setDetail_amount(String detail_amount) {
        this.detail_amount = detail_amount;
    }

    public String getIs_read() {
        return is_read;
    }

    public void setIs_read(String is_read) {
        this.is_read = is_read;
    }
}
