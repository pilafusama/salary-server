package com.tenpay.wxwork.salary.config;

public enum Range {

    VIDEO_MAX_SIZE(30 * 1024 * 1024), // 活体检测视频大小上限
    OCR_IMAGE_MAX_SIZE(1024 * 1024); // OCR 接口限制图片大小上限

    private int value;

    private Range(int value) {
        this.value = value;
    }

    public int toInt() {
        return value;
    }
}